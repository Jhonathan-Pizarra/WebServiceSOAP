    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        String txtD1=txtDiaUno.getText();
        String txtD2=txtDiaDos.getText();
        String txtD3=txtDiaTres.getText();
        String txtD4=txtDiaCuatro.getText();
        String txtD5=txtDiaCinco.getText();
        String txtP6=txtPagoHoras.getText();
        
        if(txtD1.equals("")||
                txtD2.equals("")||
                txtD3.equals("")||
                txtD4.equals("")||
                txtD5.equals("")||
                txtP6.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Ingrese todos lo datos, por favor.","Advertencia",JOptionPane.WARNING_MESSAGE);
        }else{
            double d1 = Double.parseDouble(txtD1);
            double d2 = Double.parseDouble(txtD2);
            double d3 = Double.parseDouble(txtD3);
            double d4 = Double.parseDouble(txtD4);
            double d5 = Double.parseDouble(txtD5);
            double pago = Double.parseDouble(txtP6);
            
            //Llamada al Web Service
            double horas = horasTrabajadas(d1, d2, d3, d4, d5);
            double suel= sueldo(horas, pago);
            
            JOptionPane.showMessageDialog(rootPane, "Horas trabajadas a la semana: "+horas+"horas, \n"+"Sueldo: $"+suel,"Resultado",JOptionPane.INFORMATION_MESSAGE, null);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

