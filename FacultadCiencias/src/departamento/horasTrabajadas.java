    public static Double horasTrabajadas(double d1, double d2, double d3, double d4, double d5){
        uddi.Formularios_Service service = new uddi.Formularios_Service();
        uddi.Formularios port = service.getFormulariosPort();
        return port.horasTrabajadas(d1, d2, d3, d4, d5);
    }
    public static Double sueldo(double pagoHoras, double diasTraba){
        uddi.Formularios_Service service = new uddi.Formularios_Service();
        uddi.Formularios port = service.getFormulariosPort();
        return port.sueldo(pagoHoras, diasTraba);
    }
    
    

